// var, let, const example


var name = 'John Doe';
console.log(name);
name = 'Steve Smith';
console.log(name);

// Init var
var greeting;
console.log(greeting);
greeting = 'Hello';
console.log(greeting);

// example of let
let name1 = 'John Doe';
console.log(name1);
name1 = 'Steve Smith';
console.log(name1);

// example of const
const name2 = 'John Doe';
console.log(name2);
name2 = 'Steve Smith';
console.log(name2);
